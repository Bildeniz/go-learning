package main

import "fmt"

func main() {
	var my_string string = "Bildeniz"
	fmt.Printf("Hi my name is: %s\n", my_string)

	var my_int int = 19
	fmt.Printf("The integer is: %d\n", my_int)

	var my_bool bool = true
	fmt.Printf("The bool is: %t\n", my_bool)

	var my_float float32 = 3.14
	fmt.Printf("PI is: %f\n", my_float)
}
